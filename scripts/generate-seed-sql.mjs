#!/usr/bin/env node

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = fileURLToPath(new URL(".", import.meta.url));
const root = resolve(scriptDir, "..");
const baseSeedPath = resolve(root, "supabase/seed.sql");
const mockSeedPath = resolve(root, "supabase/mock-data.sql");
const outputPath = resolve(root, "lib/seed/seed-sql.generated.ts");

const baseSeed = readFileSync(baseSeedPath, "utf8").trimEnd();
const mockSeed = readFileSync(mockSeedPath, "utf8")
  .split(/\r?\n/)
  .filter((line) => line.trim() !== "\\i seed.sql")
  .join("\n")
  .trimStart();

const seedSql = `${baseSeed}\n\n${mockSeed}\n`;
const fileContents = [
  "// This file is generated by scripts/generate-seed-sql.mjs.",
  "// Do not edit by hand.",
  `export const SEED_SQL = ${JSON.stringify(seedSql)};`,
  ""
].join("\n");

mkdirSync(resolve(root, "lib/seed"), { recursive: true });
writeFileSync(outputPath, fileContents);
